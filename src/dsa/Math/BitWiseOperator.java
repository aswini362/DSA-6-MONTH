package dsa.Math;

public class BitWiseOperator {

    public static void main(String[] args) {
        int a=13;
        int rightShift = a >> 1;
        System.out.println(rightShift);
        int b=7;
        int c=a&b;
        System.out.println(c);

        int d=-14;
        int i = d >> 1;
        System.out.println(i);

    }
}
